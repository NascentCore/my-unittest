def add_numbers(a: float, b: float) -> float:
    """
    将两个数字相加并返回结果
    
    Args:
        a (float): 第一个数字
        b (float): 第二个数字
        
    Returns:
        float: 两个数字的和
    """
    return a + b

def multiply_numbers(a: float, b: float) -> float:
    """
    将两个数字相乘并返回结果
    
    Args:
        a (float): 第一个数字
        b (float): 第二个数字
        
    Returns:
        float: 两个数字的积
    """
    return a * b 